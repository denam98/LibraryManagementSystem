    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jTable1.getSelectedRow() != -1 && jTable1.getSelectedRowCount() == 1) {
            panel_viewEmp.setVisible(false);
            panel_updateEmp.setVisible(true);

            this.empId = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());

            String fname = jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString();
            txt_fname.setText(fname);

            String lname = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
            txt_lname.setText(lname);

            String nic = jTable1.getValueAt(jTable1.getSelectedRow(), 6).toString();
            txt_nic.setText(nic);

            String phone = jTable1.getValueAt(jTable1.getSelectedRow(), 7).toString();
            txt_phone.setText(phone);

            try {
                ResultSet search = DBConnect.search("select * from employee where id='" + empId + "'");
                if (search.first()) {
                    txt_dob.setText(search.getString("dob"));
                    if (search.getString("gender").equals("M")) {
                        rb_male.setSelected(true);
                    } else {
                        rb_female.setSelected(true);
                    }
                    txt_add_home.setText(search.getString("home"));
                    txt_add_street.setText(search.getString("street"));
                    txt_add_zone.setText(search.getString("zone"));
                    txt_add_city.setText(search.getString("city"));
                    txt_add_postal.setText(search.getString("postalcode"));
                }
            } catch (ClassNotFoundException | SQLException ex) {
                SystemLogger.initLogger(AddViewEmployee.class).error("Exception from SELECT query", ex);
            }

            String role = jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString();
            switch (role) {
                case "DIR":
                    cb_role.setSelectedIndex(1);
                    break;
                case "TECH":
                    cb_role.setSelectedIndex(2);
                    break;
                case "ASSIS_LIB":
                    cb_role.setSelectedIndex(3);
                    break;
                case "LIB_AID":
                    cb_role.setSelectedIndex(4);
                    break;
                default:
                    break;
            }

            if (jTable1.getValueAt(jTable1.getSelectedRow(), 8).toString().equals("Active")) {
                rb_active.setSelected(true);
            } else {
                rb_inactive.setSelected(true);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Select an employee from the table to view / update.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

