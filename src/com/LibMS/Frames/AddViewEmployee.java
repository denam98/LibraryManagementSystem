    private void updateEmp() {
        if (checkContents()) {
            String fname = txt_fname.getText().toLowerCase();
            String lname = txt_lname.getText().toLowerCase();
            String nic = txt_nic.getText().toLowerCase();
            String phone = txt_phone.getText();
            String comboSelection = cb_role.getItemAt(cb_role.getSelectedIndex());
            String gender = (rb_male.isSelected()) ? "M" : "F";
            String dob = txt_dob.getText();
            String home = txt_add_home.getText().toLowerCase();
            String street = txt_add_street.getText().toLowerCase();
            String zone = txt_add_zone.getText().toLowerCase();
            String city = txt_add_city.getText().toLowerCase();
            String postal = txt_add_postal.getText();
            int role = 0;
            try {
                ResultSet search = DBConnect.search("select * from role where roletype='" + comboSelection + "'");
                if (search.next()) {
                    role = search.getInt("id");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                SystemLogger.initLogger(AddViewEmployee.class).error("Exception from SELECT query", ex);
            }
            int stat = (rb_active.isSelected()) ? 1 : 0;
            try {
                DBConnect.iud("update employee set role_id='" + role + "',fname='" + fname + "',"
                        + "lname='" + lname + "',nic='" + nic + "',phone='" + phone + "',dob='" + dob + "',"
                        + "gender='" + gender + "',home='" + home + "',street='" + street + "',zone='" + zone + "',"
                        + "city='" + city + "',postalcode='" + postal + "',stat='" + stat + "' where id='" + empId + "'");
            } catch (ClassNotFoundException | SQLException ex) {
                SystemLogger.initLogger(AddViewEmployee.class).error("Exception from UPDATE query", ex);
            }
            JOptionPane.showMessageDialog(rootPane, "Successfull !");
            new AddViewEmployee().setVisible(true);
            this.dispose();
        }
    }

    private boolean checkContents() {
        if (txt_fname.getText().trim().equals("")) {
            txt_fname.grabFocus();
            allDone = false;
        } else if (txt_lname.getText().trim().equals("")) {
            txt_lname.grabFocus();
            allDone = false;
        } else if (txt_nic.getText().trim().equals("") || !(txt_nic.getText().trim().length() == 10 || txt_nic.getText().trim().length() == 12)) {
            txt_nic.grabFocus();
            allDone = false;
        } else if (txt_phone.getText().trim().equals("") || txt_phone.getText().trim().length() != 10) {
            txt_phone.grabFocus();
            allDone = false;
        } else if (txt_dob.getText().trim().equals("") || txt_dob.getText().trim().length() != 10) {
            txt_dob.grabFocus();
            allDone = false;
        } else if (txt_add_home.getText().trim().equals("")) {
            txt_add_home.grabFocus();
            allDone = false;
        } else if (txt_add_street.getText().trim().equals("")) {
            txt_add_street.grabFocus();
            allDone = false;
        } else if (txt_add_zone.getText().trim().equals("")) {
            txt_add_zone.grabFocus();
            allDone = false;
        } else if (txt_add_city.getText().trim().equals("")) {
            txt_add_city.grabFocus();
            allDone = false;
        } else if (txt_add_postal.getText().trim().equals("") || txt_add_postal.getText().trim().length() != 5) {
            txt_add_postal.grabFocus();
            allDone = false;
        } else if (cb_role.getSelectedIndex() == 0) {
            cb_role.grabFocus();
            cb_role.setPopupVisible(true);
            allDone = false;
        } else {
            allDone = true;
        }
        return allDone;
    }

