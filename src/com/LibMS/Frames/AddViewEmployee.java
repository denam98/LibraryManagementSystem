    private void loadTableData() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        try {
            ResultSet search = DBConnect.search("select * from employee");
            while (search.next()) {
                Vector v = new Vector();
                v.add(search.getInt("id"));
                ResultSet search1 = DBConnect.search("select * from role where id='" + search.getInt("role_id") + "'");
                String roleType = null;
                if (search1.next()) {
                    roleType = search1.getString("roletype");
                }
                v.add(roleType);
                v.add(search.getString("fname"));
                v.add(search.getString("lname"));
                if (search.getString("gender").equals("M")) {
                    v.add("Male");
                } else {
                    v.add("Female");
                }
                v.add(search.getString("dob"));
                v.add(search.getString("nic"));
                v.add(search.getString("phone"));
                String stat = (search.getInt("stat") == 1) ? "Active" : "Inactive";
                v.add(stat);
                v.add(search.getString("dateemployeed"));
                dtm.addRow(v);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            SystemLogger.initLogger(AddViewEmployee.class).error("", ex);
        }
    }

