    private void loadMemNIC() {
        sp_memNIC.setVisible(true);
        String searchTxt = txt_memNIC.getText();
        String query = "SELECT * FROM member WHERE nic LIKE '" + searchTxt + "%'";
        try {
            ResultSet search = DBConnect.search(query);
            Vector v = new Vector();
            list_memNIC.setModel(new DefaultListModel());
            while (search.next()) {
                v.add(search.getString("nic") + "-" + search.getString("lname"));
            }
            list_memNIC.setListData(v);
        } catch (ClassNotFoundException | SQLException ex) {
            SystemLogger.initLogger(LendBook.class).error("Exception from SELECT query", ex);
        }
    }

    private void loadIsbn() {
        sp_isbn.setVisible(true);
        String searchTxt = txt_isbn.getText();
        String query = "SELECT * FROM book WHERE isbn LIKE '" + searchTxt + "%' and availableqty!=0";
        try {
            ResultSet search = DBConnect.search(query);
            Vector v = new Vector();
            list_isbn.setModel(new DefaultListModel());
            while (search.next()) {
                v.add(search.getString("isbn") + "-" + search.getString("name"));
            }
            list_isbn.setListData(v);
        } catch (ClassNotFoundException | SQLException ex) {
            SystemLogger.initLogger(LendBook.class).error("Exception from SELECT query", ex);
        }
    }

    private void lendBook() {
        if (lendCheckContents()) {
            String isbn = txt_isbn.getText();
            int member_id = Integer.parseInt(txt_memId.getText());
            Date date = jCalendar1.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String due_date = sdf.format(date);
            String sysuser = Integer.toString(SystemData.getSysUserId());

            String query = "insert into lendbook(book_isbn,member_id,lend_date,due_date,sysuser_id)"
                    + " values('" + isbn + "','" + member_id + "',CURDATE(),'" + due_date + "','" + sysuser + "')";
            try {
                DBConnect.iud(query);
                DBConnect.iud("UPDATE book set availableqty=availableqty-1 where isbn='"+isbn+"'");
            } catch (ClassNotFoundException | SQLException ex) {
                SystemLogger.initLogger(LendBook.class).error("Exception from UPDATE query", ex);
            }
            JOptionPane.showMessageDialog(rootPane, "Successful!");
            clearFields();
        }
    }

    private boolean lendCheckContents() {
        if (txt_isbn.getText().trim().equals("") || !(txt_isbn.getText().trim().length() == 13)) {
            txt_isbn.grabFocus();
            allDoneLend = false;
        } else if (txt_memNIC.getText().trim().equals("") || !(txt_memNIC.getText().trim().length() == 10 || txt_memNIC.getText().trim().length() == 12)) {
            txt_memNIC.grabFocus();
            allDoneLend = false;
        } else {
            allDoneLend = true;
        }
        return allDoneLend;
    }

