    private void txt_memNICKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_memNICKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            list_memNIC.grabFocus();
            list_memNIC.setSelectedIndex(0);
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (list_memNIC.getFirstVisibleIndex() != -1) {
                list_memNIC.grabFocus();
                list_memNIC.setSelectedIndex(0);
            } else {
                String message = "Member Not Found. Add as a new member?";
                String title = "Member Not Found";
                int option = JOptionPane.showOptionDialog(rootPane, message, title,
                        JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, JOptionPane.OK_OPTION);
                if (option == JOptionPane.OK_OPTION) {
                    AddViewMember add = new AddViewMember();
                    add.but_addMem.doClick();
                    add.setVisible(true);
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_txt_memNICKeyPressed

