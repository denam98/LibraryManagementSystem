    private void list_memNICKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_list_memNICKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            txt_memNIC.setText(list_memNIC.getSelectedValue().substring(0, list_memNIC.getSelectedValue().indexOf("-")));
            try {
                ResultSet search = DBConnect.search("select id from member where nic = '" + txt_memNIC.getText() + "'");
                if (search.next()) {
                    txt_memId.setText(Integer.toString(search.getInt("id")));
                }
            } catch (ClassNotFoundException | SQLException ex) {
                SystemLogger.initLogger(LendBook.class).error("Exception from SELECT query", ex);
            }
            sp_memNIC.setVisible(false);
            jCalendar1.grabFocus();
        }
    }//GEN-LAST:event_list_memNICKeyPressed

