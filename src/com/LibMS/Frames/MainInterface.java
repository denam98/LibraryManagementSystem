    private void roleCheck() {
        Layer_dir.setVisible(false);
        Layer_assis_lib.setVisible(false);
        Layer_tech.setVisible(false);
        Layer_lib_aid.setVisible(false);
        switch (SystemData.getRole()) {
            case "DIR":
                //All Interfaces
                Layer_dir.setVisible(true);
                break;
            case "TECH":
                //Book Store
                //Realtime Report Viewer
                //System User Management
                Layer_tech.setVisible(true);
                break;
            case "ASSIS_LIB":
                //Book Store
                //Recieve Books Back
                //Lend Book
                //Member Management
                //Pending Resubmissions
                Layer_assis_lib.setVisible(true);
                break;
            case "LIB_AID":
                //Book Store
                //Lend Book
                //Recieve Books Back
                //Pending Re-Submissions
                Layer_lib_aid.setVisible(true);

                break;
            default: {
                try {
                    throw new InvalidRoleException("Role field is empty.");
                } catch (InvalidRoleException ex) {
                    SystemLogger.initLogger(MainInterface.class).error("Custom built Exception", ex);
                }
            }
        }
    }
