    private void label_name3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_label_name3MouseClicked
        Date today = new Date();
        SimpleDateFormat dateFormat = new SimpleDateFormat("YYYY-MM-dd");
        int id = 0;
        try {
            //System.out.println(dateFormat.format(today));
            ResultSet search = DBConnect.search("select id from systemlog where login like '" + dateFormat.format(today) + "%'");
            if (search.last()) {
                id = search.getInt("id");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            SystemLogger.initLogger(MainInterface.class).error("Exception from SELECT query for systemlog relation.", ex);
        }

        try {
            DBConnect.iud("UPDATE systemlog set logout=CURRENT_TIMESTAMP() WHERE systemuser_id='" + SystemData.getSysUserId() + "' and login like '" + dateFormat.format(today) + "%' and id='" + id + "'");
        } catch (ClassNotFoundException | SQLException ex) {
            SystemLogger.initLogger(MainInterface.class).error("Exception from UPDATE query to systemlog relation.", ex);
        }
        SystemData.setCurrentUserInstance(null, null, 0);
        SystemData.flushCurrentUser();
        new LogIn().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_label_name3MouseClicked

