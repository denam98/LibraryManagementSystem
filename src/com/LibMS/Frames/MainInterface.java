    private void setImage() {
        try {
            String path = "pics/profile.png";
            Image image = ImageIO.read(new FileInputStream(path));
            image = image.getScaledInstance(lab_image.getWidth(), lab_image.getHeight(), Image.SCALE_SMOOTH);
            lab_image.setIcon(new ImageIcon(image));
        } catch (IOException ex) {
            SystemLogger.initLogger(MainInterface.class).error("IOException from setImage().", ex);
            System.out.println(ex);
        }
        try {
            ResultSet search = DBConnect.search("select s.employee_id,e.fname from employee e left join systemuser s on s.employee_id=e.id where s.id='" + SystemData.getSysUserId() + "'");
            if (search.first()) {
                label_name.setText("Hello " + search.getString("fname") + " !");
            }
            label_role.setText(SystemData.getRole());
        } catch (ClassNotFoundException | SQLException ex) {
            SystemLogger.initLogger(MainInterface.class).error("Exception from SELECT query in setImage().", ex);
        }
    }

